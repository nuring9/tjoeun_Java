package Day02;

public class DataTypeTest {
    static void main() {
        // 실행문 명령문 시작
        /*
            기본자료형
            정수형: int
            문자형: char
            실수형: double
            논리형: boolean
         */
        int a = 10;
        System.out.println(a); // 10
        char ch = 'a';
        System.out.println(ch); // 'a'
        double d = 14.78;
        System.out.println(d);  // 14.78
        boolean check = false;
        System.out.println(check);  // false
    }
}
